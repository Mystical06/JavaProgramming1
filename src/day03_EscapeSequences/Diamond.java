package day03_EscapeSequences;
/*
Task:3. Create a class called Diamond.java, and print:

                    ^
                   / \
                  /   \
                 /     \
                /       \
                ---------
                \       /
                 \     /
                  \   /
                   \ /
                    V
 */
public class Diamond {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("\t\t\t    ^");
        System.out.println("\t\t\t   / \\");
        System.out.println("\t\t\t  /   \\");
        System.out.println("\t\t\t /     \\");
        System.out.println("\t\t\t/       \\");
        System.out.println("\t\t\t---------");
        System.out.println("\t\t\t\\       /");
        System.out.println("\t\t\t \\     /");
        System.out.println("\t\t\t  \\   /");
        System.out.println("\t\t\t   \\ /");
        System.out.println("\t\t\t    v");
    }






}
